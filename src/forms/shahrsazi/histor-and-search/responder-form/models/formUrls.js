/**
 * Extract default forms' parameters
 * @param task
 * @returns {{readOnly: boolean}}
 */
function basicInitializer (task) {
  return {
    props: {
      readOnly: true,
      NidProc: task.NidProc,
      District: Number(task.NosaziCode.split('-')[0]) || 0
    }
  }
}

